package ru.baranov.task8.num2;

public class Calculator {

    public double countPlus(double var1, double var2) {
        return (var1 + var2);
    }

    public double countMinus(double var1, double var2) {
        return (var1 - var2);
    }

    public double countDiv(double var1, double var2) {
        return (var1 / var2);
    }

    public double countMul(double var1, double var2) {
        return (var1 * var2);
    }

    public double countProc(double var1, double var2) {
        return ((var1 * 100) / var2);
    }
}
